    "#分层处理，分为五层\n",
    "def assign_layers(df, factor_col, n_layers=5, by_industry=False):\n",
    "    df = df.copy()\n",
    "\n",
    "    if by_industry:\n",
    "        df['rank'] = df.groupby(['date', 'name1'])[factor_col].rank(pct=True)\n",
    "    else:\n",
    "        df['rank'] = df.groupby('date')[factor_col].rank(pct=True)\n",
    "\n",
    "    df['layer'] = (df['rank'] * n_layers).astype(int).clip(0, n_layers - 1)\n",
    "    return df\n",
    "\n",
    "#市值加权\n",
    "def calc_layer_returns(df):\n",
    "    df = df.copy()\n",
    "    df['weighted_ret'] = df['return'] * df['market_value']\n",
    "\n",
    "    grouped = df.groupby(['date', 'layer'])\n",
    "    layer_returns = grouped['weighted_ret'].sum() / grouped['market_value'].sum()\n",
    "\n",
    "    return layer_returns.unstack()\n",
    "\n",
    "#计算各项指标：最大回撤，夏普，年化\n",
    "def calculate_metrics(layer_returns, annual_days=252):\n",
    "    metrics = {}\n",
    "\n",
    "    cum_returns = (1 + layer_returns).cumprod()\n",
    "\n",
    "    for col in layer_returns.columns:\n",
    "        daily = layer_returns[col].dropna()\n",
    "        cum = cum_returns[col].dropna()\n",
    "\n",
    "        if len(daily) == 0:\n",
    "            continue\n",
    "\n",
    "        total = cum.iloc[-1] - 1\n",
    "        n = len(daily)\n",
    "\n",
    "        ann_ret = (1 + total) ** (annual_days / n) - 1\n",
    "        ann_vol = daily.std() * np.sqrt(annual_days)\n",
    "        sharpe = ann_ret / ann_vol if ann_vol != 0 else 0\n",
    "\n",
    "        max_dd = (cum / cum.cummax() - 1).min()\n",
    "\n",
    "        metrics[col] = {\n",
    "            '年化收益率': ann_ret,\n",
    "            '夏普': sharpe,\n",
    "            '最大回撤': max_dd\n",
    "        }\n",
    "\n",
    "    # 多空策略\n",
    "    top = layer_returns.columns.max()\n",
    "    bottom = layer_returns.columns.min()\n",
    "\n",
    "    long_short = layer_returns[bottom] - layer_returns[top]\n",
    "\n",
    "    cum_ls = (1 + long_short).cumprod()\n",
    "    total_ls = cum_ls.iloc[-1] - 1\n",
    "    n = len(long_short)\n",
    "\n",
    "    ann_ls = (1 + total_ls) ** (annual_days / n) - 1\n",
    "    vol_ls = long_short.std() * np.sqrt(annual_days)\n",
